create database Academia;
use Academia;

CREATE TABLE ALUNO (
	MATRICULA INT PRIMARY KEY AUTO_INCREMENT,
    NOME VARCHAR(200) NOT NULL,
    CPF VARCHAR (14) NOT NULL,
    EMAIL VARCHAR(255) NOT NULL,
    SENHA VARCHAR (255) NOT NULL
);

