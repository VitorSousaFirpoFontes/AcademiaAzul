CREATE TABLE PROFESSOR (
	MATRICULA_PF INT PRIMARY KEY AUTO_INCREMENT,
    NOME_PF VARCHAR(200) NOT NULL,
    CPF_PF VARCHAR (11) NOT NULL,
    EMAIL_PF VARCHAR(255) NOT NULL,
    SENHA_PF VARCHAR (255) NOT NULL
);

CREATE TABLE AULAS (
	ID_TREINO INT PRIMARY KEY AUTO_INCREMENT,
    DATA_TREINO VARCHAR (100) NOT NULL,
    TREINOS ENUM("Peito", "Biceps", "Triceps", "Costas", "Perna", "Membros Superiores") NOT NULL,
    REPETICOES VARCHAR (20)
);

